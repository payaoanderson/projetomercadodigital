        <p class="bloco-texto">
            No Mercado Digital, estamos comprometidos em proteger sua privacidade e garantir a segurança das suas informações pessoais. Esta política explica como coletamos, usamos, protegemos e compartilhamos suas informações ao utilizar nosso site.

1. Coleta de Informações

1.1 Informações Pessoais:
Coletamos informações como nome, endereço de e-mail, endereço de entrega, número de telefone e informações de pagamento ao criar uma conta, fazer compras, participar de promoções ou interagir conosco.

1.2 Informações Automáticas:
Utilizamos cookies e tecnologias similares para coletar informações sobre seu uso do site, como seu endereço IP, tipo de navegador, páginas visitadas e tempo de visita. Esses dados nos ajudam a melhorar sua experiência e personalizar conteúdos.

2. Uso das Informações

2.1 Processamento de Pedidos e Serviços:
Utilizamos suas informações para processar pedidos, realizar transações financeiras, enviar confirmações de pedidos, gerenciar entregas e fornecer suporte ao cliente.

2.2 Personalização e Melhoria:
Analisamos dados para entender melhor como você utiliza nosso site, adaptando-o às suas preferências, melhorando nossos produtos e serviços, e desenvolvendo novos recursos.

2.3 Comunicações:
Podemos enviar informações sobre produtos, promoções, atualizações ou ofertas especiais que consideramos ser do seu interesse, com seu consentimento. Você pode optar por não receber essas comunicações a qualquer momento.

2.4 Segurança e Proteção:
Implementamos medidas técnicas e organizacionais para proteger suas informações contra acesso não autorizado, uso indevido, alteração ou divulgação.

3. Compartilhamento de Informações

3.1 Com Terceiros de Confiança:
Compartilhamos informações com parceiros de serviços que nos auxiliam na operação do site, como processadores de pagamento, empresas de logística e provedores de serviços de hospedagem. Esses parceiros têm acesso limitado às suas informações e as utilizam apenas para prestar os serviços contratados.

3.2 Cumprimento Legal e Segurança:
Podemos divulgar suas informações pessoais se exigido por lei ou se acreditarmos de boa-fé que essa divulgação é necessária para cumprir com obrigações legais, proteger nossos direitos ou propriedade, investigar fraudes ou proteger a segurança pessoal dos usuários.

4. Seus Direitos

4.1 Acesso e Controle:
Você pode acessar, corrigir, atualizar ou excluir suas informações pessoais através das configurações da sua conta no site ou entrando em contato conosco diretamente. Responderemos às solicitações de maneira oportuna, conforme exigido por lei.

4.2 Cookies e Tecnologias Semelhantes:
Você pode gerenciar o uso de cookies através das configurações do seu navegador. Desabilitar cookies pode afetar a funcionalidade do site, mas você pode ajustar suas preferências conforme necessário.

5. Alterações na Política

Esta política de privacidade pode ser atualizada periodicamente para refletir mudanças em nossas práticas de informações. Publicaremos qualquer alteração significativa nesta página e, se apropriado, notificaremos você por e-mail. Incentivamos você a revisar esta política regularmente para estar informado sobre como protegemos suas informações.

6. Contato e Mais Informações

Para mais informações sobre nossa política de privacidade ou para exercer seus direitos de proteção de dados, entre em contato conosco pelo e-mail: digitalmercado@mercadodigitalstore.com
